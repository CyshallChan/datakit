package main

import (
	"bytes"
	"io/ioutil"
	"net/http"
	"sync"
	"time"

	"github.com/gin-gonic/gin"
)

func startHTTP() {
	router := gin.New()

	router.Use(gin.Recovery())

	router.POST("/v1/write/:category",
		func(c *gin.Context) {
			_, _ = ioutil.ReadAll(c.Request.Body)
			c.Request.Body.Close()
			c.Status(http.StatusOK)
		})

	srv := &http.Server{
		Addr:    "localhost:12345",
		Handler: router,
	}

	if err := srv.ListenAndServe(); err != nil {
		panic(err)
	}
}

func main() {
	go func() {
		startHTTP()
	}()

	time.Sleep(time.Second)

	wg := sync.WaitGroup{}
	worker := 20

	reqs := map[string][]byte{
		"http://localhost:9529/v1/write/logstreaming?type=influxdb":          logstreamingData,
		"http://localhost:9529/v1/write/logging?input=post-v1-write-logging": loggingData,
		"http://localhost:9529/v1/write/metric?input=post-v1-write-metric":   metricData,
	}

	wg.Add(worker)
	for i := 0; i < worker; i++ {
		go func() {
			defer wg.Done()
			cli := http.Client{}

			n := 0
			for {
				for k, v := range reqs {
					req, err := http.NewRequest("POST", k, bytes.NewBuffer(v))
					if err != nil {
						continue
					}

					resp, err := cli.Do(req)
					if err != nil {
						continue
					}

					_, _ = ioutil.ReadAll(resp.Body)

					resp.Body.Close()
					n++

					if n%100 == 0 {
						time.Sleep(time.Second)
					}
				}
			}
		}()
	}

	wg.Wait()
}

